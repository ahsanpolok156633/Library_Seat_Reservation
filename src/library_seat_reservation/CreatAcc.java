    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
          String pass=jPasswordField2.getText();
         String uid = jTextField1.getText();
        String name=jTextField2.getText();
        String email=jTextField5.getText();
        String uname=jTextField6.getText();
        String query = "INSERT INTO `student` VALUES ('"+uid+"','"+name+"','"+email+"','"+pass+"','"+uname+"');";
	Connection con = null;
        Statement st = null;
        
        
        if(jTextField1.getText().isEmpty())
        {
        JOptionPane.showMessageDialog(this, "Please Enter Student ID!");
        }
        else if(jTextField2.getText().isEmpty())
        {
        JOptionPane.showMessageDialog(this, "Please Enter Your Name!");
        }
        else if(jTextField5.getText().isEmpty())
        {
        JOptionPane.showMessageDialog(this, "Please Enter Your e-mail!");
        }
        else if(jTextField6.getText().isEmpty())
        {
        JOptionPane.showMessageDialog(this, "Please Enter Your user name!");
        }
        else if(jPasswordField2.getText().isEmpty())
        {
        JOptionPane.showMessageDialog(this, "Please Enter Your password!");
        }
        
        else{
         try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/library", "root", "");
            st = con.createStatement();
            st.execute(query);
            JOptionPane.showMessageDialog(this, "Account Created!");
           // this.setVisible(false);
           close();
            new Ulogin().setVisible(true);
            
            
        }
        catch(Exception ex) {
	    JOptionPane.showMessageDialog(this, "Failed to create account!");
            System.out.println("Exception : " + ex.getMessage());
        }
        finally {
            try {
                if(st!=null)
					st.close();

                if(con!=null)
					con.close();
            }
            catch(Exception ex) {}
        }
       
        }
    }//GEN-LAST:event_jButton1ActionPerformed

